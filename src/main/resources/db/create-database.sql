  CONSTRAINT `FOREIGN_KEY_RL_ACCOUNTS_PERMISSIONS_PERMISSION`
    FOREIGN KEY (`PERMISSION` )
    REFERENCES `usm`.`permissions` (`ID` )
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);


