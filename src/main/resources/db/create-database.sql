  INDEX `FOREIGN_KEY_RL_ACCOUNTS_PERMISSIONS_PERMISSION_IDX` (`PERMISSION` ASC) ,
  CONSTRAINT `FOREIGN_KEY_RL_ACCOUNTS_PERMISSIONS_ACCOUNT`
